using System;
using System.Runtime.InteropServices;

namespace Gobchat.Core.UI
{
    // App-local Win32 interop. The Gobchat.UI assembly has its own NativeMethods, but it is internal to
    // that assembly, so the tray-menu z-order fix (see NotifyIconManager) needs these here.
    internal static class NativeMethods
    {
        public static readonly IntPtr HWND_TOPMOST = new IntPtr(-1);

        public const uint SWP_NOSIZE = 0x0001;
        public const uint SWP_NOMOVE = 0x0002;
        public const uint SWP_NOACTIVATE = 0x0010;

        [DllImport("user32.dll", SetLastError = true)]
        [return: MarshalAs(UnmanagedType.Bool)]
        public static extern bool SetWindowPos(IntPtr hWnd, IntPtr hWndInsertAfter, int X, int Y, int cx, int cy, uint uFlags);
    }
}
